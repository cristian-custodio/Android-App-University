package com.cristiancustodio.usmapp;



public class BachelorListItem {
    private String head;
    private String desc;

    public BachelorListItem(String head, String desc) {
        this.head = head;
        this.desc = desc;
    }

    public String getHead() {
        return head;
    }

    public String getDesc() {
        return desc;
    }
}
